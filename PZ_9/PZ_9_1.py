# Вариант 13.
# Книжные магазины предлагают следующие коллекции книг.
# Магистр – Лермонтов, Достоевский, Пушкин, Тютчев
# ДомКниги – Толстой, Грибоедов, Чехов, Пушкин.
# БукМаркет – Пушкин, Достоевский, Маяковский.
# Галерея – Чехов, Тютчев, Пушкин.
# Определить:
# 1.в каких магазинах можно одновременно приобрести книги Достоевского и Пушкина.
# 2.в ассортимент магазина ДомКниги добавить автора Лермонтов.
# 3.какие книги из магазина БукМаркет отсутствуют в магазине Магистр.

magistr = {"Лермонтов", "Достоевский", "Пушкин", "Тютчев"}
domknigi = {"Толстой", "Грибоедов", "Чехов", "Пушкин"}
bookmarket = {"Пушкин", "Достоевский", "Маяковский"}
galereya = {"Чехов", "Тютчев", "Пушкин"}

# 1.В каких магазинах можно одновременно приобрести книги Достоевского и Пушкина.

need_books = {'Достоевский', 'Пушкин'}
needs_shops = []

if need_books.issubset(magistr):
    needs_shops.append("Магистр")
if need_books.issubset(domknigi):
    needs_shops.append("ДомКниги")
if need_books.issubset(bookmarket):
    needs_shops.append("БукМаркет")
if need_books.issubset(galereya):
    needs_shops.append("Галерея")

print("Магазины, в которых есть и Достоевский, и Пушкин:")
if needs_shops:
    print(f"   {', '.join(needs_shops)}")
else:
    print("Таких магазинов нет")

# 2.В ассортимент магазина ДомКниги добавить автора Лермонтов.

domknigi.add('Лермонтов')
print("После добавления 'Лермонтов' в ДомКниги:")
print(f"   ДомКниги: {domknigi}")

# 3.Какие книги из магазина БукМаркет отсутствуют в магазине Магистр.

magist_books = bookmarket - magistr
print("Книги из БукМаркет, которых нет в Магистре:")
if magist_books:
    print(f"   {magist_books}")
else:
    print("Все книги БукМаркет есть в Магистре")